import UIKit

extension UIColor {
    
    class var ourDarkGreen: UIColor {
        
        return UIColor.init(red: (40/255), green: (107/255), blue: (71/255), alpha: 1.0)

    }
    
    class var ourShadowColor: UIColor {
        
        return UIColor.init(red: (72/255), green: (107/255), blue: (88/255), alpha: 1.0)
        
    }
    
}
